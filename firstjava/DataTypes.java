package firstjava;

public class DataTypes {
	public static void main(String[] args) {
		// long with suffix l or L
		long l = 1000l;
		float f = 3.14f;
		int hexa = 0xCAFE;
		int octal = 010;
		int binary = 0b1001;
		int million = 1_000_000;
		System.out.println("Long " + l + " Hexa " + hexa + " octal " + octal + " Binary " + binary +" Underscore " + million);
		
	}

}
